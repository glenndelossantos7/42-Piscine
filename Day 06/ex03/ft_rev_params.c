void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int n;

	n = argc - 1;
	while (n > 0)
	{
		ft_putstr(argv[n]);
		ft_putchar('\n');
		n--;
	}
	return (0);
}
